#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *res;
	char cc;

	cc = (char)c;
	res = NULL;
	while (*s)
	{
		if (*s == cc)
			res = (char *)s;
		s++;
	}
	if (*s == cc)
		res = (char *)s;
	return (res);
}
